using System;
public class DisadvantageAdvantage {
    private int id;
    private string name;
    private int pointInit;
    private int pointContinue;
    private string description;

    public string Name { get => name; set => name = value; }
    public int PointInit { get => pointInit; set => pointInit = value; }
    public int PointContinue { get => pointContinue; set => pointContinue = value; }
    public string Description { get => description; set => description = value; }
    public int Id { get => id; set => id = value; }
}
